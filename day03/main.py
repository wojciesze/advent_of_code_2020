data=[]

def read_input(array):
    for x in array.split("\n"):
        if x == '' or x.startswith(" "):
            continue
        l = []
        l[:0] = x
        data.append(l)

def calculate(right, down):
    col_idx = right
    row_idx = down

    trees_cnt = 0
    for idx in range(len(data)):
        try:
            line = data[row_idx]
        except IndexError:
            break

        if col_idx >= len(line):
            col_idx = col_idx - len(line)

        if line[col_idx] == '#':
            trees_cnt += 1

        col_idx += right
        row_idx += down

    return trees_cnt

def part1():
    print('[Part 1] Total trees: ', calculate(3, 1))

def part2():
    t_11 = calculate(1, 1)
    t_31 = calculate(3, 1)
    t_51 = calculate(5, 1)
    t_71 = calculate(7, 1)
    t_12 = calculate(1, 2)
    print('[Part 2] Total trees (1,1): ', t_11)
    print('[Part 2] Total trees (3,1): ', t_31)
    print('[Part 2] Total trees (5,1): ', t_51)
    print('[Part 2] Total trees (7,1): ', t_71)
    print('[Part 2] Total trees (1,2): ', t_12)
    print('[Part 2] Final result = ', t_11 * t_31 * t_51 * t_71 * t_12)


if __name__ == '__main__':
    read_input(input)
    part1()
    part2()


